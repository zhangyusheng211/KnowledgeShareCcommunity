<template>
  <div class="app-container">
    <el-form :model="queryParams" ref="queryForm" :inline="true" v-show="showSearch" label-width="68px">
      <el-form-item label="姓名" prop="name">
        <el-input
          v-model="queryParams.name"
          placeholder="请输入姓名"
          clearable
          size="small"
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
     <el-form-item label="描述" prop="description">
        <el-input
          v-model="queryParams.description"
          placeholder="请输入描述"
          clearable
          size="small"
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>

      <el-form-item>
        <el-button type="primary" icon="el-icon-search" size="mini" @click="handleQuery">搜索</el-button>
        <el-button icon="el-icon-refresh" size="mini" @click="resetQuery">重置</el-button>
      </el-form-item>
    </el-form>

    <el-row :gutter="10" class="mb8">
      <el-col :span="1.5">
        <el-button
          type="primary"
          icon="el-icon-plus"
          size="mini"
          @click="handleAdd"
        >添加老师</el-button>
      </el-col>

      <el-col :span="1.5">
        <el-button
          type="danger"
          icon="el-icon-delete"
          size="mini"
          :disabled="multiple"
          @click="handleDelete"
        >删除</el-button>
      </el-col>
      <right-toolbar :showSearch.sync="showSearch" @queryTable="getList"></right-toolbar>
    </el-row>

    <el-table v-loading="loading" :data="actorList" @selection-change="handleSelectionChange">
      <el-table-column type="selection" width="55" align="center" />
      <el-table-column align="center" label="序号" width="60">
        <template slot-scope="scope">
          <span>{{scope.$index + 1}}</span>
        </template>
      </el-table-column>
      <el-table-column label="姓名" align="center" prop="name"  width="150" />
      <el-table-column prop="avatar" label="头像" align="center" width="80">
        <template slot-scope="scope">
          <el-image class="imagesList" :src="fileUploadHost + scope.row.avatar" lazy />
        </template>
      </el-table-column>
      <el-table-column prop="description" label="简述" align="center" width="300"   show-overflow-tooltip  ></el-table-column>

      <el-table-column label="标签 " align="center" prop="label" :formatter="labelFormat" width="200" />

      <el-table-column label="操作" align="center" width="250"  fixed="right" class-name="small-padding fixed-width">
        <template slot-scope="scope">
          <el-button
            size="mini"
            type="primary"
            icon="el-icon-edit"
            @click="handleUpdate(scope.row)"
          >编辑</el-button>
          <el-button
            size="mini"
            type="danger"
            icon="el-icon-delete"
            @click="handleDelete(scope.row)"
          >删除</el-button>
        </template>
      </el-table-column>
    </el-table>

    <pagination
      v-show="total>0"
      :total="total"
      :page.sync="queryParams.currentPage"
      :limit.sync="queryParams.pageSize"
      @pagination="getList"
    />

    <!-- 添加或修改演员对话框 -->
    <el-dialog :title="title" :visible.sync="open" width="960px" append-to-body>
      <el-form ref="form" :model="form" :rules="rules" label-width="80px">

        <el-row>
          <el-col :span="16">
            <el-form-item label="姓名" prop="name"  >
              <el-input v-model="form.name" style="width: 50%" placeholder="请输入姓名" />
            </el-form-item>
            <el-form-item label="标签"   prop="label" >
              <el-select v-model="labelList" multiple style="width: 60%"  placeholder="请选择标签">
                <el-option
                  v-for="dict in labelOptions"
                  :key="dict.dictValue"
                  :label="dict.dictLabel"
                  :value="dict.dictValue">
                  <span style="float: left">{{ dict.dictLabel }}</span>
                  <span style="float: right; color: #8492a6; font-size: 13px">{{ dict.dictValue }}</span>
                </el-option>
              </el-select>
            </el-form-item>
            <el-form-item label="简述" prop="description">
              <el-input v-model="form.description" rows="4" type="textarea" placeholder="请输入奖项内容" maxlength="1000" show-word-limit />
            </el-form-item>
            <el-form-item label="奖项" prop="awards">
              <editor v-model="form.awards" :height="200"/>
            </el-form-item>
          </el-col>
        <el-col :span="8">
          <el-form-item label="" class="images-uploader"  :label-width="formLabelWidth" prop="avatar">
            <imageUpload :limit="1" v-model="form.avatar"/>
<!--            <el-upload
              class="el-upload"
              :action="commonUploadImagesUrl"
              :show-file-list="false"
              :on-success="handleImagesSuccess"
              :before-upload="beforeImagesUpload"
              :headers="headers">
              <img v-if="form.avatar" :src="fileUploadHost + form.avatar" class="imagesDetail">
              <i v-else class="el-icon-plus images-uploader-icon"></i>
            </el-upload>-->
          </el-form-item>
        </el-col>
        </el-row>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button type="primary" @click="submitForm">确 定</el-button>
        <el-button @click="cancel">取 消</el-button>
      </div>
    </el-dialog>
  </div>
