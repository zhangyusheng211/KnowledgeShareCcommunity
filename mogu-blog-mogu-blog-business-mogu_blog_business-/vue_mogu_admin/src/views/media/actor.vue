<script>
  import { listActor, getActor, delActor, addActor, updateActor } from "@/api/mediaActor";
  import { getListByDictTypeList } from "@/api/sysDictData"
  export default {
    name: "Actor",
    components: {
    },
    data() {
      return {
        commonUploadImagesUrl: '',
        fileUploadHost: process.env.FILE_API,
        headers: {
          Authorization: "Bearer ",
        },
        formLabelWidth: "80px",
        // 遮罩层
        loading: true,
        // 选中数组
        ids: [],
        // 非单个禁用
        single: true,
        // 非多个禁用
        multiple: true,
        // 显示搜索条件
        showSearch: true,
        // 总条数
        total: 0,
        // 演员表格数据
        actorList: [],
        // 弹出层标题
        title: "",
        // 是否显示弹出层
        open: false,
        // 查询参数
        queryParams: {
          currentPage: 1,
          pageSize: 10,
          name: null,
          avatar: null,
          description: null,
          awards: null,
          label: null
        },
        //标签类型
        labelOptions:[],
        //标签数据
        labelList:[],
        // 表单参数
        form: {},
        // 表单校验
        rules: {
          name: [
            { required: true, message: "姓名不能为空", trigger: "blur" }
          ],
         /* avatar:[{ required: true, message: "必须上传一张图片", trigger: "blur" }],*/
          label:[{ required: true, message: "标签不能为空", trigger: "blur" }]
        }
      };
    },
    created() {
      const dictTypeList =  ['actor_label'];
      getListByDictTypeList(dictTypeList).then(response => {
        this.labelOptions = response.data.actor_label.list;
      });
      this.getList();
    },
    methods: {
      //标签类型字典翻译
      labelFormat(row, column){
        if (!row.label) return '';
        return this.selectDictLabels(this.labelOptions, row.label);
      },
      handleImagesSuccess(res, file) {
        const code = res.code;
        if (code === 200) {
          this.form.avatar =  res.url;
          this.$commonUtil.message.success("上传成功！");
        } else {
          this.$commonUtil.message.error(res.message);
        }
      },
      beforeImagesUpload(file) {
        console.log(file.type);
        const isImages = (file.type === 'image/jpeg') || (file.type === 'image/png');
        const isLt10M = file.size / 1024 / 1024 < 10;
        if (!isImages) {
          this.$commonUtil.message.error('上传头像图片只能是 JPG 格式 和 PNG 格式!');
        }
        if (!isLt10M) {
          this.$commonUtil.message.error('上传头像图片大小不能超过 10MB!');
        }
        return isImages && isLt10M;
      },
      /** 查询演员列表 */
      getList() {
        this.loading = true;
        listActor(this.queryParams).then(response => {
          if (response.code == this.$ECode.SUCCESS) {
              this.actorList = response.data.records;
              this.total = response.data.total;
              this.loading = false;
          } else {
              this.$message.error("查询出错")
          }
        });
      },
      // 取消按钮
      cancel() {
        this.open = false;
        this.reset();
      },
      // 表单重置
      reset() {
        this.form = {
          uid: null,
          name: null,
          avatar: null,
          description: null,
          awards: null,
          label: null,
          status: 1
        };
        this.resetForm("form");
      },
      /** 搜索按钮操作 */
      handleQuery() {
        this.queryParams.currentPage = 1;
        this.getList();
      },
      // 表单重置
      resetForm(refName) {
        if (this.$refs[refName]) {
          this.$refs[refName].resetFields();
        }
      },
      /** 重置按钮操作 */
      resetQuery() {
        this.resetForm("queryForm");
        this.handleQuery();
      },
      // 多选框选中数据
      handleSelectionChange(selection) {
        this.ids = selection.map(item => item.uid)
        this.single = selection.length!==1
        this.multiple = !selection.length
      },
      /** 新增按钮操作 */
      handleAdd() {
        this.reset();
        this.open = true;
        this.title = "添加演员";
        this.labelList=[];
      },
      /** 修改按钮操作 */
      handleUpdate(row) {
        this.reset();
        let params = {}
        params.uid = row.uid || this.ids;
        getActor(params).then(response => {
          this.form = response.data;
          this.open = true;
          this.title = "修改演员";
          let that = this;
          that.labelList = [];
          let dbLabelList = that.form.label.split(",");
          for (let index = 0; index < dbLabelList.length; index++) {
            if (dbLabelList[index] != null && dbLabelList[index] !== "") {
              that.labelList.push(dbLabelList[index]);
            }
          }
        });
      },
      /** 提交按钮 */
      submitForm() {
        if(this.labelList.length <= 0) {
          this.$commonUtil.message.error("标签不能为空!");
          return;
        }
        var that = this;
        that.form.label = that.labelList.join(",");
        this.$refs["form"].validate(valid => {
          if (valid) {
            if (this.form.uid != null) {
              updateActor(this.form).then(response => {
                this.$commonUtil.message.success("修改成功");
                this.open = false;
                this.getList();
              });
            } else {
              addActor(this.form).then(response => {
                this.$commonUtil.message.success("新增成功");
                this.open = false;
                this.getList();
              });
            }
          }
        });
      },
      /** 删除按钮操作 */
      handleDelete(row) {
        let actorIds = '';
        if (row.uid){
          actorIds = [row.uid];
        } else {
          actorIds = this.ids
        }

        this.$confirm('是否确认删除演员编号为"' + actorIds + '"的数据项?', "警告", {
          confirmButtonText: "确定",
          cancelButtonText: "取消",
          type: "warning"
        }).then(function() {
          return delActor(actorIds);
        }).then(() => {
          this.getList();
          this.$commonUtil.message.success("删除成功");
        })
      },
      // 回显数据字典
      selectDictLabel(datas, value) {
        var actions = [];
        Object.keys(datas).some((key) => {
          if (datas[key].dictValue == ('' + value)) {
            actions.push(datas[key].dictLabel);
            return true;
          }
        })
        return actions.join('');
      },
     // 回显数据字典（字符串数组）
      selectDictLabels(datas, value, separator) {
        let actions = [];
        const currentSeparator = undefined === separator ? "," : separator;
        const temp = value.split(currentSeparator);
        Object.keys(value.split(currentSeparator)).some((val) => {
          Object.keys(datas).some((key) => {
            if (datas[key].dictValue == ('' + temp[val])) {
              actions.push(datas[key].dictLabel + currentSeparator);
            }
          })
        })
        return actions.join('').substring(0, actions.join('').length - 1);
      }
    }
  };
</script>
